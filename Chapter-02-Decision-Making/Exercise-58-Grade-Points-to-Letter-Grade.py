a = float(input("Enter the grade point: "))
if a >= 4.0:
    print("A+")
elif a > 3.7:
    print("A")
elif a > 3.3:
    print("A-")
elif a > 3.0:
    print("B+")
elif a > 2.7:
    print("B")
elif a > 2.3:
    print("B-")
elif a > 2.0:
    print("C+")
elif a > 1.7:
    print("C")
elif a > 1.3:
    print("C-")
elif a > 1.0:
    print("D+")
elif a > 0:
    print("D")
elif a == 0:
    print("F")
